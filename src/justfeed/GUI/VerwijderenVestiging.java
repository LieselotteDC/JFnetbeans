    private void btnJAVerwijderVestigingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJAVerwijderVestigingActionPerformed
        String takeAwayNaam = txtTakeAwayNaam.getText();
        String vestigingsNaam = txtVestigingsNaam.getText();
        if(takeAwayNaam.isEmpty() || vestigingsNaam.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Gelieve een take-away en vestiging in te vullen.");
            if (takeAwayNaam .isEmpty()){
            txtTakeAwayNaam.requestFocus();
            }
            else if(vestigingsNaam.isEmpty()){
            txtVestigingsNaam.requestFocus();
            }
        }
        else{
            if(d.bestaatVestiging(vestigingsNaam, takeAwayNaam)){
                Vestiging v = d.getVestiging(takeAwayNaam, vestigingsNaam);
                d.deleteVestiging(v);
                JOptionPane.showMessageDialog(null,"De vestiging werd succesvol verwijderd.");
            }
            else{
                JOptionPane.showMessageDialog(null,"Deze vestiging bestaat niet. Probeer opnieuw.");
                txtTakeAwayNaam.setText("");
                txtVestigingsNaam.setText("");
                txtTakeAwayNaam.requestFocus();
                txtVestigingsNaam.requestFocus();
            }
                    
        }
    }//GEN-LAST:event_btnJAVerwijderVestigingActionPerformed

