    private void btnJAVerwijderProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJAVerwijderProductActionPerformed
        if(txtProductID.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Gelieve een productID in te vullen.");
        }
        else{
            int productID = Integer.parseInt(txtProductID.getText());
            if(d.productIDBestaat(productID)){
                Product p = d.getProduct(productID);
                d.deleteProduct(p);
                JOptionPane.showMessageDialog(null,"Het product werd succesvol verwijderd.");
                txtProductID.setText("");
                Administrator admini = Administrator.getInstance(verwijderenVanProduct);
                admini.pack();
                verwijderenVanProduct.hide();
                admini.show();
                admini.setLocationRelativeTo(null);
            }
            else{
                JOptionPane.showMessageDialog(null,"Dit productnummer bestaat niet. Probeer opnieuw.");
                txtProductID.setText("");
                txtProductID.requestFocus();
            }
                    
        }
    }//GEN-LAST:event_btnJAVerwijderProductActionPerformed

