    private void btnAanmakenMaandelijkseKortingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAanmakenMaandelijkseKortingActionPerformed
        String maand = comboboxMaand.getSelectedItem().toString();
        if(txtJaar.getText().isEmpty() || maand.isEmpty() ){
            JOptionPane.showMessageDialog(null, "Gelieve alle verplichte velden in te vullen.");
            if (txtJaar.getText().isEmpty()){
            txtJaar.requestFocus();
            }
            else if(maand.isEmpty()){
            comboboxMaand.requestFocus();
            }
        }
        else{
            int jaar = Integer.parseInt(txtJaar.getText());
            d.addKortingJustfeedBoss(maand, jaar);
            d.addKortingTakeawayBoss(maand, jaar);
            JOptionPane.showMessageDialog(null, "Uw kortingscode werd succesvol toegevoegd.");
            Administrator administrator = Administrator.getInstance(maandelijkseKorting);
//     amdministrator.setSize(300,300);
            administrator.pack();
            maandelijkseKorting.hide();
            administrator.show();
            administrator.setLocationRelativeTo(null);
            comboboxMaand.setSelectedItem(null);
            txtJaar.setText("");
        }
        
    }//GEN-LAST:event_btnAanmakenMaandelijkseKortingActionPerformed

